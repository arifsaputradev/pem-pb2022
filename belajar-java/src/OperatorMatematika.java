public class OperatorMatematika {
    public static void main(String[] args) {
        int a = 10;
        int b = 7;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

        // Augmented Assignments
        System.out.println(a+=10);
        System.out.println(a-=10);
        System.out.println(a*=10);
        System.out.println(a/=10);
        System.out.println(a%=10);

        // Unary Operator
        // ++ = a=a+1
        // -- = a=a-1
        // - = negatif
        // + = positif
        // ! = boolean kebalikan
        System.out.println(!true);
    }
}
